// ====== 判断食材是否属于常备品 ======
export function isStaple(name: string): boolean {
  // 精确匹配或派生词匹配（"盐末""盐的"等）
  return STAPLE_EXCLUSIONS.some(
    (s) => name === s || name.startsWith(s + "的") || name.startsWith(s + "末") || name.startsWith(s + "粉")
  )
}

// ====== 菜名→主食材拆解 ======
export function decomposeDishName(name: string): string[] {
  for (const [dish, ingredients] of Object.entries(DISH_DECOMPOSE_MAP)) {
    if (name.includes(dish)) return ingredients
  }
  return [name]
}

// ====== 判断食材属于哪个分类 ======
export function classifyIngredient(name: string): string {
  for (const cat of classifyMap) {
    // 精确关键词匹配（优先）
    if (cat.keywords && cat.keywords.some((k) => name.includes(k))) {
      if (cat.excludeKeywords && cat.excludeKeywords.some((k) => name.includes(k))) continue
      return cat.name
    }
    // 低优先级关键词匹配（前缀/后缀/完全相等——避免误伤）
    if (cat.keywordsLow && cat.keywordsLow.some((k) => name === k || name.endsWith(k) || name.startsWith(k))) {
      if (cat.excludeKeywords && cat.excludeKeywords.some((k) => name.includes(k))) continue
      return cat.name
    }
  }
  return "其他"
}

// ====== 食材名称归一化：去掉数量后缀和括号克数 ======
export function normalizeIngredientName(raw: string): string {
  let cleaned = raw.trim()
  // 先去掉末尾括号里的克数/重量，如(300g)、(1斤)、(500克)
  cleaned = cleaned.replace(/\s*\([^)]*[\d.][^)]*\)\s*$/, '').trim()
  // 循环去除数量后缀，直到稳定（处理 "豆腐一块100g" → "豆腐" 等多层情况）
  const qtyRegex = /^\s*(.+?)\s*(?:[\d.]+\s*(?:个|只|片|根|条|块|克|斤|两|盒|袋|包|瓶|罐|kg|g|ml|l|勺|碗|杯|份|瓣|颗|粒|段|半|少许|适量|若干|少量)|[一二三四五六七八九十两半]+\s*(?:个|只|片|根|条|块|克|斤|两|盒|袋|包|瓶|罐|瓣|颗|粒|段|少许|适量|若干|少量))\s*$/
  let prev: string
  do {
    prev = cleaned
    cleaned = cleaned.replace(qtyRegex, '$1').trim()
  } while (cleaned !== prev)
  return cleaned || raw.trim()
}